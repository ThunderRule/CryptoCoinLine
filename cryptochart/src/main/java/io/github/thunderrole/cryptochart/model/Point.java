package io.github.thunderrole.cryptochart.model;

/**
 * 功能描述：
 *
 * @date 2022/1/5
 */
public class Point {
    private float x;
    private float y;

    public Point(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }
}
